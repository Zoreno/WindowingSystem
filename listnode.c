#include "listnode.h"

#include <stdint.h>
#include <stdlib.h>


ListNode *ListNode_new(void *payload)
{
    ListNode *list_node;
    
    if(!(list_node = (ListNode *)malloc(sizeof(ListNode))))
        return list_node;

    list_node->prev = (ListNode *)0;
    list_node->next = (ListNode *)0;
    list_node->payload = payload;

    return list_node;
}


